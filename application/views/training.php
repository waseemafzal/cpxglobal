<h2>COURSE OVERVIEW:</h2>

<p>Achieving predictable print results with high accuracy, full automation, cost efficiency and accomplish consistent colour reproduction are the challenging expectations of clients in the rotogravure printing industry. This increasing demands of customer only attainable when print manufacturer has superlative accuracy of engraved cylinders that are vulnerable to defects such as stylus broken, shoe lines, cell depth variation, cell missing, thundering, centre out, pin holes, bludges, patches, during the engraving process resulted into degradation of quality as well as rejection of cylinders. This one day training session addresses various topics on cylinder engraving and quality management through hands-on activities accompanied with case studies, presentations and discussion suitable anyone in operations, quality, sales, customer service and marketing etc. associate with gravure printing industry and at the end participants will leave the class with a strong understanding of the advance learning skills of cylinder quality management and be able to troubleshoot a variety of cylinder related issues upon return to their respective facility.</p>

<h2>TRAINING CONTENTS:</h2>

<p>Gravure printing, impact of engraved cylinder on print performance, cylinder structure, category, composition and properties.<br />
Steps involve in preparation of best quality engraved cylinders, critical process parameters, variables and engraving patterns.<br />
Cylinders engraving workflow, chemical etching, electromechanical engraving and direct ablation of Gravure cylinders.<br />
Engraving cylinder axis, shaft, diameter, circumference, face length and electroplating to engrave high quality cylinders.<br />
Measurement of the actual engrave cell opening, depth and cell volume on copper, and subsequently chrome cells, to control quality and consistency during the manufacturing process.<br />
Effects of engraving cell depth, cell wall, opening, bridge, cell angle and composition of cylinder and process Specifications.<br />
Relation of cell size to dot size, ink transfer from cell, Moir&eacute; Effect and cell specifications impact on print quality and production<br />
Engraved cylinders defects visualizing and measuring in detail the performance of the engraving process to further improve quality consistency<br />
Gravure ink formulation, substrate selection, doctor blades angle and composition on the performance of engraved cylinders.&nbsp;<br />
Best safe practices and techniques for handling, transportation and storage of engraved cylinders in workplace.</p>

